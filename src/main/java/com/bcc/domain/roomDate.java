package com.bcc.domain;

public class roomDate {

	private String sel_date;
	
	private String sel_date2;

	public String getSel_date() {
		return sel_date;
	}

	public void setSel_date(String sel_date) {
		this.sel_date = sel_date;
	}

	public String getSel_date2() {
		return sel_date2;
	}

	public void setSel_date2(String sel_date2) {
		this.sel_date2 = sel_date2;
	}

	@Override
	public String toString() {
		return "roomDate [sel_date=" + sel_date + ", sel_date2=" + sel_date2 + "]";
	}
	
	
	
	
}
